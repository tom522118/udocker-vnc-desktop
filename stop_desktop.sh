#!/bin/bash

# ==========================================================
# 腳本名稱: stop_desktop.sh
# 功能描述: 安全停止 udocker 桌面環境並清理殘留檔案
# ==========================================================

echo "----------------------------------------------------------"
echo " [1/2] 正在停止相關進程 (udocker, vnc, websockify)..."
echo "----------------------------------------------------------"

# 1. 使用 fuser 強制釋放 6080 埠號
if command -v fuser &> /dev/null; then
    fuser -k 6080/tcp 2>/dev/null
fi

# 2. 強制終止所有相關程式
pkill -9 -f udocker 2>/dev/null
pkill -9 -f websockify 2>/dev/null
pkill -9 -f vnc 2>/dev/null

echo " >>> 進程已終止."

echo "----------------------------------------------------------"
echo " [2/2] 正在清理系統殘留鎖定檔..."
echo "----------------------------------------------------------"

# 3. 清理鎖定檔 (防止下次啟動時埠號跳轉)
rm -rf /tmp/.X*-lock /tmp/.X11-unix/X* /tmp/.ICE-unix/

echo " >>> 鎖定檔清理完成."

echo "----------------------------------------------------------"
echo " ✅ 桌面環境已完全關閉。"
echo " 若要重新啟動，請執行: bash start_desktop.sh"
echo "----------------------------------------------------------"
